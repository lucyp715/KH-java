package ncs.test6;

public class EmptyException extends Exception {

	public EmptyException() {
		super();
	}

	public EmptyException(String message, Throwable cause) {
		super(message, cause);
	}

	public EmptyException(String message) {
		super(message);
	}

	public EmptyException(Throwable cause) {
		super(cause);
	}

	
}
